/* Fibonacci Series
 *  Returns n-th number in the Fibonacci series
 *
 * Author : NuclearBanane
 * Contributors : Stickler
 * Version : v0.1.2
 */
function fib(n){
    return (n==0 || n==1) ? 1 : fib(n-1) + fib(n-2);
}

/* Adder function
 *  returns the result of the addition a + b
 * 
 * Author : NuclearBanane
 * Contributors : 
 * Version : v0.1
 */ 
function add(a,b) {
    return a+b;
}

